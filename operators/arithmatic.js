//arithmatic operators

// The basic arithmetic operators in JavaScript are:
// 1. Addition (+): This operator is used to add two numbers together. For example:
let a = 5;
let b = 10;
let sum = a + b;
console.log(sum); // Output: 15, because the + operator adds the values of a and b together.

// 2. Subtraction (-): This operator is used to subtract one number from another. For example:
let difference = b - a;
console.log(difference); // Output: 5, because the - operator subtracts the value of a from b.

// 3. Multiplication (*): This operator is used to multiply two numbers together. For example:
let product = a * b;
console.log(product); // Output: 50, because the * operator multiplies the values of a and b together.

// 4. Division (/): This operator is used to divide one number by another. For example:
let quotient = b / a;
console.log(quotient); // Output: 2, because the / operator divides the value of b by a.

// 5. Modulus (%): This operator is used to find the remainder of a division operation. For example:
let remainder = b % a;
console.log(remainder); // Output: 0, because the % operator finds the remainder of dividing b by a, which is 0 in this case.

// 6. Exponentiation (**): This operator is used to raise a number to the power of another number. For example:
let power = a ** 2;
console.log(power); // Output: 25, because the ** operator raises the value of a to the power of 2, which is 25 in this case.
// It is important to understand how these arithmetic operators work in JavaScript, as they are fundamental to performing mathematical operations in your code. Always make sure to use the appropriate operator for the operation you want to perform, and be aware of the order of operations when combining multiple operators in a single expression.

